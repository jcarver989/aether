/// Returns all planning-agent evals defined programmatically
pub fn all_evals() -> Result<Vec<Eval>, Box<dyn std::error::Error>> {
    Ok(vec![
        // Joist ORM - Issue 1406: Optional schema configuration
        Eval::new(
            "joist_easy_issue_1406",
            load_prompt("joist/easy/issue-1406")?,
            WorkingDirectory::git_repo(
                "https://github.com/joist-orm/joist-orm",
                "215c15f99380d3864b58201e31a7614c02d2a366",
                "ac4ac099ad0c667020267f16ee81652cf3d4b181",
                None::<&str>,
            )?,
            vec![ten_point_scorer()?],
        )
        .before_assertions(ClaudeCode::new("plan.md")),
    ])
}

fn load_prompt(eval_path: &str) -> Result<String, Box<dyn std::error::Error>> {
    let tests_dir = PathBuf::from(env!("CARGO_MANIFEST_DIR")).join("tests");
    let prompt_path = tests_dir.join("evals").join(eval_path).join("prompt.md");
    let prompt = Prompt::file(prompt_path.to_str().ok_or("Invalid path")?, false).build()?;
    Ok(prompt)
}

fn ten_point_scorer() -> Result<EvalAssertion, AgentError> {
    Ok(EvalAssertion::llm_judge(|ctx| {
        let gold_commit = match ctx.working_dir {
            crucible::WorkingDirectory::GitRepo { gold_commit, .. } => Some(gold_commit.as_str()),
            _ => None,
        };

        let args = HashMap::from([
            (
                "1".to_string(),
                ctx.working_dir.path().display().to_string(),
            ),
            ("2".to_string(), ctx.original_prompt.to_string()),
            (
                "3".to_string(),
                ctx.git_diff(gold_commit).unwrap_or_default(),
            ),
            ("4".to_string(), ctx.git_diff(None).unwrap_or_default()),
            ("5".to_string(), EvalMetric::json_schema()),
        ]);

        Prompt::file_with_args("./src/ten_point_scorer.md", false, args)
            .build()
            .expect("Failed to build prompt")
    }))
}

/// Hook that reads a plan file and executes it using Claude Code CLI
pub struct ClaudeCode {
    prompt_file: String,
}

impl ClaudeCode {
    pub fn new(plan_file: impl Into<String>) -> Self {
        Self {
            prompt_file: plan_file.into(),
        }
    }
}

impl BeforeAssertionsHook for ClaudeCode {
    fn run(
        &self,
        input: BeforeAssertionsHookInput,
    ) -> Pin<Box<dyn Future<Output = HookResult> + Send>> {
        let plan_file = self.prompt_file.clone();
        Box::pin(async move {
            // 1. Read plan file from working directory into a string (should be generated by our agent)
            let plan_path = input.working_directory.join(&plan_file);
            let plan_contents = fs::read_to_string(&plan_path)
                .map_err(|e| format!("Failed to read {}: {}", plan_file, e))?;

            tracing::info!("Starting Claude Code agent");

            // 2. Pass plan contents as prompt to Claude Code via CLI command: $ claude -p <plan contents> --verbose
            // 3. Stream output and await claude command finishing
            use tokio::io::{AsyncBufReadExt, BufReader};

            let mut child = tokio::process::Command::new("claude")
                .arg("-p")
                .arg("--dangerously-skip-permissions")
                .arg("--verbose")
                .arg("--output-format")
                .arg("stream-json")
                .arg(format!("An engineer has produced this implementation plan: <plan>{}</plan>. Implement the feature per the plan.", plan_contents))
                .current_dir(&input.working_directory)
                .stdout(std::process::Stdio::piped())
                .stderr(std::process::Stdio::piped())
                .spawn()
                .map_err(|e| format!("Failed to spawn claude CLI: {}", e))?;

            // Stream stdout in real-time
            if let Some(stdout) = child.stdout.take() {
                let reader = BufReader::new(stdout);
                let mut lines = reader.lines();

                tokio::spawn(async move {
                    while let Ok(Some(line)) = lines.next_line().await {
                        tracing::info!("claude: {}", line);
                    }
                });
            }

            // Wait for the process to complete
            let status = child
                .wait()
                .await
                .map_err(|e| format!("Failed to wait for claude CLI: {}", e))?;

            if !status.success() {
                return Err(format!("Claude CLI failed with status {}", status).into());
            }

            tracing::info!("Claude Code successfully executed the plan");

            Ok(())
        })
    }
}
